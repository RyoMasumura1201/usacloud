DOCKER_IMAGE_NAME="usacloud-lint"
DOCKER_CONTAINER_NAME="usacloud-lint-container"

if [[ $(docker ps -a | grep $DOCKER_CONTAINER_NAME) != "" ]]; then
  docker rm -f $DOCKER_CONTAINER_NAME 2>/dev/null
fi

docker build -f scripts/textlint.dockerfile -t $DOCKER_IMAGE_NAME .

docker run -ti --rm \
       $DOCKER_IMAGE_NAME .
